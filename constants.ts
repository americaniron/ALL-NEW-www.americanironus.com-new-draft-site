const getCategoryFromModel = (model: string): string => {
  const m = model.toUpperCase();
  if (m.startsWith('D') && !isNaN(parseInt(m[1]))) return 'DOZERS'; 
  if (m.match(/^3\d{2}/) || m.match(/^3\d{2}\./)) return 'EXCAVATORS'; 
  if (m.match(/^9\d{2}/)) {
    if (['953', '963', '973'].some(t => m.includes(t))) return 'TRACK LOADERS';
    return 'WHEEL LOADERS';
  }
  if (m.startsWith('7') && m.length >= 3) {
    if (['725', '730', '735', '740', '745'].some(t => m.startsWith(t))) return 'ARTICULATED TRUCKS';
    if (['770', '772', '773', '775', '777'].some(t => m.startsWith(t))) return 'OFF-HIGHWAY TRUCKS';
  }
  if (m.startsWith('120') || m.startsWith('140') || m.startsWith('150') || m.startsWith('160') || m.startsWith('12M') || m.startsWith('14M') || m.startsWith('16M')) return 'MOTOR GRADERS';
  if (m.startsWith('CS') || m.startsWith('CP') || m.startsWith('CB') || m.startsWith('CW')) return 'COMPACTORS'; 
  if (m.startsWith('415') || m.startsWith('416') || m.startsWith('420') || m.startsWith('430')) return 'BACKHOE LOADERS';
  if (m.startsWith('TL') || m.startsWith('TH')) return 'TELEHANDLERS';
  if (m.startsWith('2') && m.length >= 3) {
    if (['239', '249', '259', '279', '289', '299'].some(t => m.startsWith(t))) return 'TRACK LOADERS';
    return 'SKID STEERS';
  }
  if (m.startsWith('61') || m.startsWith('62') || m.startsWith('63')) return 'SCRAPERS';
  if (m.startsWith('RM')) return 'ROAD RECLAIMERS';
  if (m.startsWith('538') || m.startsWith('548') || m.startsWith('558')) return 'FORESTRY';
  
  return 'MISC / ATTACHMENTS';
};

